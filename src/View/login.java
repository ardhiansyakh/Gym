    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        String username = inpuUser.getText().trim();
        String password = new String(jPasswordField1.getPassword()).trim();

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username dan Password tidak boleh kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (username.length() < 4 || password.length() < 4) {
            JOptionPane.showMessageDialog(this, "Username dan Password minimal 4 karakter.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            User user = new User();
            User loggedUser = user.login(username, password);

            if (loggedUser != null) {
                if ("admin".equalsIgnoreCase(loggedUser.role)) {
                    new Adminmenu(loggedUser.id, loggedUser.nama, loggedUser.role).setVisible(true);
                } else if ("member".equalsIgnoreCase(loggedUser.role)) {
                    new Usermenu(loggedUser.id, loggedUser.nama).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Role tidak dikenali: " + loggedUser.role, "Error", JOptionPane.ERROR_MESSAGE);
                }
                this.dispose();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat login:\n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

