    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = inputUser.getText().trim();
        String password = new String(inputPas.getPassword()).trim();
        String paket = (String) jComboBox1.getSelectedItem();
        String role = "member"; 

        if (username.isEmpty() || password.isEmpty() || paket == null) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (username.length() <  4|| password.length() < 4) {
            JOptionPane.showMessageDialog(this, "Username dan Password minimal 4 karakter.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!paket.equalsIgnoreCase("mingguan") && !paket.equalsIgnoreCase("bulanan")) {
            JOptionPane.showMessageDialog(this, "Tipe paket tidak valid.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            User user = new User();
            boolean success = user.register(username, password, role, paket);

            if (success) {
                inputUser.setText("");
                inputPas.setText("");
                jComboBox1.setSelectedIndex(0);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat registrasi:\n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

